CREATE TABLE history (
	uuid BINARY(16) NOT NULL,
	origin BINARY(16) NOT NULL,

	client_ip VARBINARY(16) NOT NULL,
	server_ip VARBINARY(16) NOT NULL,

	client_lat DECIMAL(10, 8) NOT NULL,
	server_lat DECIMAL(10, 8) NOT NULL,

	client_lng DECIMAL(11, 8) NOT NULL,
	server_lng DECIMAL(11, 8) NOT NULL,

	amount SMALLINT DEFAULT 1,
	created TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
	updated TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	
	PRIMARY KEY (uuid),
	FOREIGN KEY (origin) REFERENCES origin(uuid) ON DELETE CASCADE
)

CREATE TABLE origin (
	uuid BINARY(16) NOT NULL,
	title VARCHAR(255) NOT NULL,

	created TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
	updated TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	
	PRIMARY KEY (uuid)
)

